print("Slice Operator")
alphabets = "abcdefghijklmnopqrstuvwxyz";
print("Alphabets of 0:5 is ",alphabets[0:5])
print("Alphabets of 5:10 is ",alphabets[5:10])
print("=================================================");
print("");


print("Alphabets of 4 to last is ",alphabets[4:]);
print("=================================================");
print("");


print("Alphabets of -1 is ",alphabets[-1:]);
print("=================================================");
print("");


print("Alphabets of -1 to -3 is ",alphabets[-1:-3]);
print("=================================================");
print("");


print("Alphabets of 2 to -1 is ",alphabets[2:-1]);
print("=================================================");
print("");


print("Alphabets of 2 to -5 is ",alphabets[2:-5]);
print("=================================================");
print("");



print("Alphabets of 0:len(alphabets) is ",alphabets[0:len(alphabets)])
